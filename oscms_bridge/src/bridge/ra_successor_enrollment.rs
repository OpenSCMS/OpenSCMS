use crate::OscmsBridgeError;
use crate::{RaSuccessorEnrollmentArgs, handle_ra_successor_enrollment};

use crate::util::{
    initialize_empty_oscms_octet_buffer, initialize_oscms_new_octet_buffer_from_vec,
    oscms_octet_buffer_to_vec,
};

use scmscommon::GlobalConfig;

pub fn ra_sucessor_enrollment_certificate_request_handler(
    sucessor_enrollment_request: Vec<u8>,
    ra_private: Vec<u8>,
    ra_enc_private: Vec<u8>,
    ra_certificate: Vec<u8>,
) -> Result<(Vec<u8>, Vec<u8>, String), OscmsBridgeError> {
    log::debug!(
        "OSCMS-BRIDGE: Starting ra_sucessor_enrollment_certificate_request_handler processing, request len: {:?}",
        sucessor_enrollment_request.len()
    );

    // Inputs
    let encoded_ee_ra_successor_enrollment_request =
        initialize_oscms_new_octet_buffer_from_vec(sucessor_enrollment_request.clone());
    let ra_private_key = initialize_oscms_new_octet_buffer_from_vec(ra_private.clone());
    let ra_enc_private_key = initialize_oscms_new_octet_buffer_from_vec(ra_enc_private.clone());
    let ra_encoded_certificate = initialize_oscms_new_octet_buffer_from_vec(ra_certificate.clone());

    let next_dl_time = GlobalConfig::global().param_successor_next_dl_time as u32;

    // outputs
    let encoded_ra_ee_ack = initialize_empty_oscms_octet_buffer();
    let decrypted_successor_enrollment_request = initialize_empty_oscms_octet_buffer();

    let mut args = RaSuccessorEnrollmentArgs {
        encoded_ee_ra_successor_enrollment_request,
        ra_private_key,
        ra_enc_private_key,
        ra_encoded_certificate,
        next_dl_time,
        request_hash: [0; 8],
        encoded_ra_ee_ack,
        decrypted_successor_enrollment_request,
    };

    // 2. Call handle_ee_ra_download_request and handle result
    let result = unsafe { handle_ra_successor_enrollment(&mut args) };
    if result != 0 {
        return Err(OscmsBridgeError::new(result));
    }

    log::debug!("Successor enrollment request decoded and processed successfully");

    // 3. Extract outputs from args
    let ack_message = oscms_octet_buffer_to_vec(&args.encoded_ra_ee_ack);
    let decoded_payload = oscms_octet_buffer_to_vec(&args.decrypted_successor_enrollment_request);

    if ack_message.is_empty() || decoded_payload.is_empty() {
        return Err(OscmsBridgeError::new(-1));
    }

    let hash_id_bytes = args.request_hash.to_vec();
    let hash_id_hex: String = hash_id_bytes
        .iter()
        .map(|byte| format!("{:02x}", byte))
        .collect();

    // 4. Return Ok if everything went well
    Ok((ack_message, decoded_payload, hash_id_hex))
}

