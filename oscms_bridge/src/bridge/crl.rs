use scmscommon::get_current_time_seconds;

pub fn make_secured_crl_encoded(
    crl_series: u16,
    crl_psid: u64,
    ra_private: Vec<u8>,
    ra_certificate: Vec<u8>,
    revoked_certificates_vec: Vec<Vec<u8>>,
) -> Result<(Vec<u8>, String), OscmsBridgeError> {
    log::debug!(
        "OSCMS-BRIDGE: Starting make_secured_crl_encoded processing, crl_series: {:?}, crl_psid: {:?}",
        crl_series,
        crl_psid,
    );

    // Input
    let mut signer_private_key = initialize_oscms_new_octet_buffer_from_vec(ra_private.clone());
    let mut signer_certificate = initialize_oscms_new_octet_buffer_from_vec(ra_certificate.clone());

    if signer_private_key.data.is_null() || signer_certificate.data.is_null() {
        unsafe {
            crate::oscms_empty_octet_buffer(&mut signer_certificate);
            crate::oscms_empty_octet_buffer(&mut signer_private_key);
        }
        return Err(OscmsBridgeError::new(
            crate::OscmsErrorCode_OSCMS_ERROR_INTERNAL_SERVER_ERROR,
        ));
    }

    let revoked_certificates: Vec<OscmsOctetBuffer> = revoked_certificates_vec
        .iter()
        .map(|v| initialize_oscms_new_octet_buffer_from_vec(v.clone()))
        .collect();

    // Output
    let encoded_secured_crl = initialize_empty_oscms_octet_buffer();

    let crl_issue_time = get_current_time_seconds();
    let response_generation_time = crl_issue_time;
    let next_crl = crl_issue_time + 86400;
    let expiry_time = crl_issue_time + 7 * 86400;

    let mut args = SecuredCrlEncodingArgs {
        crl_series,
        crl_psid,
        crl_issue_time,
        response_generation_time,
        next_crl,
        expiry_time,
        revoked_certificates: revoked_certificates.as_ptr(),
        revoked_certificates_count: revoked_certificates_vec.len(),
        signer_private_key,
        signer_certificate,
        craca_id: [0; 8],
        encoded_secured_crl,
    };

    let result = unsafe { encode_secured_crl(&mut args) };
    if result != 0 {
        unsafe {
            crate::oscms_empty_octet_buffer(&mut args.signer_certificate);
            crate::oscms_empty_octet_buffer(&mut args.signer_private_key);
            crate::oscms_empty_octet_buffer(&mut args.encoded_secured_crl);
        }
        return Err(OscmsBridgeError::new(result));
    }

    let encoded = oscms_octet_buffer_to_vec(&args.encoded_secured_crl);
    if encoded.is_empty() {
        log::debug!("OSCMS-BRIDGE: make_secured_crl_encoded failed, encoded is empty");
        unsafe {
            crate::oscms_empty_octet_buffer(&mut args.signer_certificate);
            crate::oscms_empty_octet_buffer(&mut args.signer_private_key);
            crate::oscms_empty_octet_buffer(&mut args.encoded_secured_crl);
        }

        return Err(OscmsBridgeError::new(-1));
    }

    // crlCraca to string
    let crl_craca = args
        .craca_id
        .iter()
        .map(|b| format!("{:02X}", b))
        .collect::<String>();

    // Free allocated memory in OctetBuffers
    unsafe {
        crate::oscms_empty_octet_buffer(&mut args.signer_certificate);
        crate::oscms_empty_octet_buffer(&mut args.signer_private_key);
        crate::oscms_empty_octet_buffer(&mut args.encoded_secured_crl);
    }

    Ok((encoded, crl_craca))
}

pub fn make_composite_crl_encoded(
    ctl_encoded: Vec<u8>,
    crl_list: Vec<Vec<u8>>,
    crl_version: u32,
) -> Result<(Vec<u8>, String), OscmsBridgeError> {
    log::debug!(
        "OSCMS-BRIDGE: Starting make_composite_crl_encoded processing, crl_version: {:?}, number of CRLs: {:?}",
        crl_version,
        crl_list.len(),
    );

    let encoded_multi_signed_ctl_spdu = initialize_oscms_new_octet_buffer_from_vec(ctl_encoded);

    let sequence_of_encoded_secured_crl: Vec<OscmsOctetBuffer> = crl_list
        .iter()
        .map(|v| initialize_oscms_new_octet_buffer_from_vec(v.clone()))
        .collect();

    let mut args = OscmsCompositeCrlSpduArgs {
        encoded_multi_signed_ctl_spdu,
        sequence_of_encoded_secured_crl: sequence_of_encoded_secured_crl.as_ptr(),
        secured_crl_count: crl_list.len(),
        encoded_composite_crl_spdu: initialize_empty_oscms_octet_buffer(),
    };

    unsafe {
        let result = encode_composite_crl_spdu(&mut args);
        if result != 0 {
            return Err(OscmsBridgeError::new(result));
        }
    }

    let encoded = oscms_octet_buffer_to_vec(&args.encoded_composite_crl_spdu);
    if encoded.is_empty() {
        log::debug!("OSCMS-BRIDGE: encode_composite_crl_spdu failed, encoded is empty");
        return Err(OscmsBridgeError::new(-1));
    }

    //  Filename:
    //  The filename is formed as composite-crl_version.oer where the following occurs:
    //  version is the minimal-length ASCII hex representation of the version of the composite CRL
    //  including CTL file that is incremented by the RA every time the file is updated.
    let crl_version_ascii_hex = format!("{:X}", crl_version);
    let filename = format!("composite-crl_{}.oer", crl_version_ascii_hex);

    Ok((encoded, filename))
}

