use crate::OscmsBridgeError;
use crate::{EeRaDownloadRequestArgs, handle_ee_ra_download_request};

use crate::util::{
    initialize_empty_oscms_octet_buffer, initialize_oscms_new_octet_buffer_from_vec,
    oscms_octet_buffer_to_vec,
};

pub fn decode_download_request_spdu(
    encoded_ee_ra_download_request: Vec<u8>,
    eca_public: [u8; 64],
    ra_private: Vec<u8>,
    ra_certificate: Vec<u8>,
    eca_certificate: Vec<u8>,
) -> Result<(u32, String), OscmsBridgeError> {
    log::debug!(
        "OSCMS-BRIDGE: Decoding download request SPDU: {:?}",
        encoded_ee_ra_download_request.len()
    );

    // Inputs
    let mut encoded_ee_ra_download_request_spdu =
        initialize_oscms_new_octet_buffer_from_vec(encoded_ee_ra_download_request.clone());
    let mut eca_public_key = initialize_oscms_new_octet_buffer_from_vec(eca_public.to_vec());
    let mut eca_encoded_certificate =
        initialize_oscms_new_octet_buffer_from_vec(eca_certificate.clone());
    let mut ra_private_key = initialize_oscms_new_octet_buffer_from_vec(ra_private.clone());
    let mut ra_encoded_certificate =
        initialize_oscms_new_octet_buffer_from_vec(ra_certificate.clone());

    if encoded_ee_ra_download_request_spdu.data.is_null()
        || eca_public_key.data.is_null()
        || eca_encoded_certificate.data.is_null()
        || ra_private_key.data.is_null()
        || ra_encoded_certificate.data.is_null()
    {
        unsafe {
            crate::oscms_empty_octet_buffer(&mut encoded_ee_ra_download_request_spdu);
            crate::oscms_empty_octet_buffer(&mut eca_public_key);
            crate::oscms_empty_octet_buffer(&mut eca_encoded_certificate);
            crate::oscms_empty_octet_buffer(&mut ra_private_key);
            crate::oscms_empty_octet_buffer(&mut ra_encoded_certificate);
        }
        return Err(OscmsBridgeError::new(
            crate::OscmsErrorCode_OSCMS_ERROR_INTERNAL_SERVER_ERROR,
        ));
    }
    // outputs
    let request_file_name = initialize_empty_oscms_octet_buffer();

    let mut args = EeRaDownloadRequestArgs {
        encoded_ee_ra_download_request_spdu,
        eca_public_key,
        ra_private_key,
        ra_encoded_certificate,
        eca_encoded_certificate,
        request_generation_time: 0,
        request_file_name,
    };

    // 2. Call handle_ee_ra_download_request and handle result
    let result = unsafe { handle_ee_ra_download_request(&mut args) };
    if result != 0 {
        unsafe {
            crate::oscms_empty_octet_buffer(&mut encoded_ee_ra_download_request_spdu);
            crate::oscms_empty_octet_buffer(&mut eca_public_key);
            crate::oscms_empty_octet_buffer(&mut eca_encoded_certificate);
            crate::oscms_empty_octet_buffer(&mut ra_private_key);
            crate::oscms_empty_octet_buffer(&mut ra_encoded_certificate);
        }
        return Err(OscmsBridgeError::new(result));
    }

    log::debug!("Download request SPDU decoded successfully");

    // Filename
    let filename_raw = oscms_octet_buffer_to_vec(&args.request_file_name);

    // 3. Prepare decoder output by extracting filename from args.filename
    let filename = String::from_utf8_lossy(&filename_raw);
    let filename = filename.split('\0').collect::<Vec<&str>>()[0];

    // Clean up buffers
    unsafe {
        // Inputs
        crate::oscms_empty_octet_buffer(&mut args.encoded_ee_ra_download_request_spdu);
        crate::oscms_empty_octet_buffer(&mut args.eca_public_key);
        crate::oscms_empty_octet_buffer(&mut args.eca_encoded_certificate);
        crate::oscms_empty_octet_buffer(&mut args.ra_private_key);
        crate::oscms_empty_octet_buffer(&mut args.ra_encoded_certificate);

        // Output
        crate::oscms_empty_octet_buffer(&mut args.request_file_name);
    }

    log::debug!(
        "Decoded download request SPDU: generationTime: {}, filename: {}",
        args.request_generation_time,
        filename
    );

    Ok((args.request_generation_time, filename.to_string()))
}

